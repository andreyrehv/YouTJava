package javaYT;

public class FluxodeSwitch {
	

	public static void main (String[] args) {

		
		char sexo = 'm';
		
		switch(sexo) {
		
			case 'M':
				System.out.println("Macho");
				break;
			case 'F':
				System.out.println("Femea");
				break;
			default:
				System.out.println("Indiferente");
		}
		
		
		
		
		
		
		
}
}